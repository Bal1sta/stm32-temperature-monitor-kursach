void TIM3_Init () {																 // TIM3 using for start conversion
	  RCC -> APB1ENR |= RCC_APB1ENR_TIM3EN; 				 // TIM 3 clock enable
		TIM3 -> CR1 = TIM_CR1_CEN;										 // TIM3 counter enable
		
		TIM3->CR2 &= ~(TIM_CR2_MMS_0 | TIM_CR2_MMS_2); 
		TIM3->CR2 |= TIM_CR2_MMS_1;										 // Enable TRGO (trigger output signal)	
		
		TIM3->DIER |= TIM_DIER_UIE;										 // Enable TIM3 interrupt
		NVIC_EnableIRQ (TIM3_IRQn);										 // Enable TIM3 interrupt	in interrupt controller
}

void ADC_TIM3_Init () {
		ADC1->CR2 &= ~ADC_CR2_ADON;  									 // ADC OFF
	
		ADC1->CR2 &= ~ADC_CR2_EXTSEL; 								 // Disable software start
		ADC1->CR2 |= ADC_CR2_EXTSEL_2;								 // Set start from TIM3 TRGO
	
		ADC1->CR2 &= ~ADC_CR2_CONT; 									 // Disable sequence conversion 
		ADC1->CR2 |= ADC_CR2_ADON; 										 // ADC ON
}


void ADC_Convert_SQ () {
	  ADC1->CR2 |= ADC_CR2_CONT;										 // Enable sequence conversion 
		ADC1->CR2 |= ADC_CR2_SWSTART; 								 // Start ADC conversion
}

void ADC_Convert_IT () {
		NVIC_EnableIRQ (ADC1_2_IRQn);									 // Enable ADC interrupt in interrupt controller
		ADC1->CR1 |= ADC_CR1_EOCIE;										 // Enable interrupt when conversion is finished
}

void ADC_Start () {
		ADC1->CR2 |= ADC_CR2_SWSTART; 								 // Start ADC conversion
}

void TIM3_IRQHandler () {													 // TIM3 interruprt handler
		TIM3_interrupts++;														 // TIM3 interrupts counter
}
